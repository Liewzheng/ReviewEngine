//! report types like [`ExpertReport`], [`AggregatedReport`],
//! [`GlobalReviewContext`]), and `finding` (review findings with
//! severity, confidence, line numbers, and recommendations). Also
//! defines top-level enums and structs such as [`RiskLevel`],
//! [`OverallAssessment`], [`Severity`], and [`ReviewInput`].

